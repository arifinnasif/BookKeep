DECLARE
        V_QUANTITY NUMBER;
    BEGIN
        SELECT QUANTITY INTO V_QUANTITY FROM BOOKS WHERE ISBN = :NEW.ISBN;
        IF V_QUANTITY > 0 THEN
            UPDATE BOOKS SET QUANTITY = QUANTITY - 1 WHERE ISBN = :NEW.ISBN;
        END IF;

